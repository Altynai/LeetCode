using int64 = long long;

template <typename T>
using min_priority_queue = priority_queue<T, vector<T>, greater<T>>;

class Solution {
public:
  vector<int> getFinalState(vector<int> &a, int k, int multiplier) {
    if (multiplier == 1)
      return a;

    int n = a.size();

    int64 max_value = *max_element(a.begin(), a.end());
    min_priority_queue<pair<int, int>> que;
    for (int i = 0; i < n; i++)
      que.push({a[i], i});

    while (k > 0 and !que.empty() and
           1LL * que.top().first * multiplier <= max_value) {
      auto [_, pos] = que.top();
      que.pop();
      a[pos] *= multiplier;
      k--;
      que.push({a[pos], pos});
    }

    // now min_value * multiplier > max_value
    // which indicates it's just a looping process now by order
    vector<int> orders;
    while (!que.empty()) {
      orders.push_back(que.top().second);
      que.pop();
    }

    vector<int> ans = a;
    for (int i = 0; i < n; i++) {
      int j = orders[i];
      int times = k / n;
      if (k % n != 0 and i < k % n)
        times++;
      ans[j] = int(mint(multiplier).pow(times) * ans[j]);
    }
    return ans;
  }
};
